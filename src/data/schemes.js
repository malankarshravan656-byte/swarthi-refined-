// ─── Helper Functions ──────────────────────────────────────────────────────

export const getSchemesByCategory = (category) => {
  if (category === 'all') return allSchemes;
  return allSchemes.filter(s => s.category === category);
};

export const getSchemesByState = (state) => {
  return allSchemes.filter(s => !s.stateSpecific || s.stateSpecific === state);
};

export const searchSchemes = (query) => {
  const q = query.toLowerCase();
  return allSchemes.filter(s =>
    s.name.toLowerCase().includes(q) ||
    s.benefits.toLowerCase().includes(q) ||
    s.category.toLowerCase().includes(q) ||
    s.ministry.toLowerCase().includes(q) ||
    (s.keywords || []).some(k => k.includes(q))
  );
};

